<?php
/*
公元年可以除以4不可整除 為平年
 公元年分除以4 可整除,為閏年
*/
$year= 2400;
if ($year%4 !=0) {
    echo    "平年";
} else {
    echo    "閏年";
}

echo "西元年".$year ;


/*
公元年除以4可整除,但除以100不可被整除 為閏年
 公元年分除以4可整除,且除以100不可被整除 為閏年
*/
echo "<br>";
$year= 2400;
if ($year%4 == 0    && $year%100 !=0) {
    echo    "閏年";
} else {
    echo    "平年";
}

echo "/西元年".$year ;

/*
公元年除以100可整除,但除以400不可被整除 為平年
 公元年分除以4可整除 且公元年除以100不可整除,或除以400可被整除 為閏年
*/

echo "<br>";
$year= 2400;
if ($year%4 == 0    &&  ($year%100 !=0  ||  $year%400 ==0)) {
    echo    "閏年";
} else {
    echo    "平年";
}

echo "/西元年".$year ;



?>